
public class VariationNaming_Exercise {
	
	public static void main(String[] args) {
	
		
		//이름:홍길동 나이:29 키:170.1를 변수에 넣어서 화면에 출력하는 프로그램을 만들어보자.
		String name ="홍길동";
		int age = 29;
		double height = 170.1;
		System.out.println("이름: "+ name + "\n나이: " + age + "\n키: " + height);
		
//		다음을 실행시켜 결과를 출력해 보자. a= 5,b=10를 넣은 다음 a,b의 합을 화면에 출력해 보자.
		int a = 5;
		int b = 10;
		System.out.println(a+b);
	}
}
